
public class OperatorsExamples {

	public static void main(String[] args) {
		int a=10;
		int b=3;
		int sum = a+b;
		int sub = a-b;
		int mul = a*b;
		int div = a/b;
		int mod = a%b;
		System.out.println("sum of to number is "+sum);
		System.out.println("sub of to number is "+sub);
		System.out.println("mul of to number is "+mul);
		System.out.println("div of to number is "+div);
		System.out.println("div of to number is "+mod);
	}

}
